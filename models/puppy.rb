
class Puppy
  def initialize(name="",breed="",age="")
    @name = name
    @breed = breed
    @age = age
  end
  def name=(name)
    @name = name
  end
  def name
    @name
  end
  def breed=(name)
    @breed = name
  end
  def breed
    @breed
  end
  def age=(name)
    @age = name
  end
  def age
    @age
  end
end
